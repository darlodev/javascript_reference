// VARIABLES

let variableOne = 1;
// 'let' is used to declare a block-scoped local variable
variableOne = "one";
// 'let' variables can have their value updated

const variableTwo = 2;
// 'const' is used to declare a block-scoped constant
// constants cannot have their value updated

var variableThree = 3;
// 'var' is used to declare a globally-scoped variable
// 'var' is often found in legacy code
variableThree = "three";
// 'var' variables can have their value updated

